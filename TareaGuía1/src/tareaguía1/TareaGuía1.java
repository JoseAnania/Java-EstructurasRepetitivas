
package tareaguía1;

public class TareaGuía1 {

    public static void main(String[] args) {
        
        int acum100=0;
        
        for (int i = 0; i <= 100; i++) {
            
            acum100+=i;
        }
        
        System.out.println("El resultado de la suma de la serie del 1 al 100 es: "+acum100);
    }
    
}
